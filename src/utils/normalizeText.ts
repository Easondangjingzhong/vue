export const normalizeText = (str: string) => {
  if (!str) return '';
  return Array.from(str).map((c: string) => {
    // 处理全角字母数字
    const code = c.charCodeAt(0);
    if (code >= 0x2E80 && code <= 0x2FDF) {
      //console.log(c)
      return cjkMap[c] || c; // 存在映射则替换，否则保留原字符
    }
    return c;
  }).join('');
};